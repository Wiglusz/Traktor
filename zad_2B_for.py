def pomnoz_przez_dwa_for(input_list):
    result = []
    for number in input_list:
        result.append(number * 2)
    return result


input_list = [1, 2, 3, 4, 5]
result_for = pomnoz_przez_dwa_for(input_list)
print(result_for)


